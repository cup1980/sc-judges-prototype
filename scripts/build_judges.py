def build_retired(existing_norms):
    retired = []
    for line in RETIRED_RAW.strip().split("\n"):
        line = line.strip()
        if not line: continue
        p = line.split("|")
        name, yomi, appointed, retired_date, prev = p[0],p[1],p[2],p[3],p[4]
        note = p[5] if len(p)>5 else ""
        norm = norm_name(name)
        if norm in existing_norms:  # 現職優先
            continue
        chokan = bool(re.search(r"\d+代長官|初代長官", note))
        retired.append({"judgeId":"ret_"+norm,"name":name,"nameNormalized":norm,
            "role":"長官" if chokan else "判事","division":None,"status":"退官",
            "reading":yomi.replace("\u3000"," ").strip(),"appointed":appointed,
            "retired":retired_date,"previousPost":prev,"note":note,"birth":None,"career":[]})
    return retired


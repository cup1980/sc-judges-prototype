import requests, re, json, time, sys
from bs4 import BeautifulSoup
from urllib.parse import urljoin

LIST_URL = "https://www.courts.go.jp/saikosai/about/saibankan/index.html"
HEADERS = {"User-Agent": "Mozilla/5.0 (SupremeCourtWatch research bot; contact: example@example.com)"}
GENGO = {"昭和": 1925, "平成": 1988, "令和": 2018}
ITAIJI = str.maketrans({"邉":"辺","邊":"辺","惠":"恵","眞":"真","槇":"槙"})

def norm_name(name):
    return (name or "").replace(" ","").replace("\u3000","").translate(ITAIJI)

def to_seireki(gengo, year):
    base = GENGO.get(gengo)
    return base + int(year) if base else None

def parse_birth(text):
    m = re.search(r"(昭和|平成|令和)(\d+|元)年(\d+)月(\d+)日生", text)
    if not m: return None
    g = m.group(1); y = 1 if m.group(2)=="元" else int(m.group(2))
    return {"gengo":g,"year":y,"month":int(m.group(3)),"day":int(m.group(4)),
            "seireki":to_seireki(g,y),"raw":m.group(0).strip("()")}

def parse_judge_page(url):
    r = requests.get(url, headers=HEADERS, timeout=30)
    r.raise_for_status(); r.encoding = r.apparent_encoding
    soup = BeautifulSoup(r.text, "html.parser")
    text = soup.get_text("\n", strip=True)
    reading = None
    mr = re.search(r"[一-龥々\s]+[(（]([ぁ-んー]+)[)）]", text)
    if mr: reading = mr.group(1)
    birth = parse_birth(text)
    career = []
    mcar = re.search(r"略歴\n(.+?)(?:信条|最高裁において関与|※)", text, re.S)
    if mcar:
        lines = [l.strip() for l in mcar.group(1).split("\n") if l.strip()]
        i = 0
        while i < len(lines):
            ym = re.match(r"(昭和|平成|令和)\s*(\d+|元)年(\d+月\d+日)?$", lines[i])
            if ym and i+1 < len(lines):
                career.append({"date":lines[i],"event":lines[i+1]}); i+=2
            else:
                if career: career[-1]["event"] += " " + lines[i]
                i+=1
    return {"reading":reading,"birth":birth,"career":career}

def crawl_current(fetch_detail=True, sleep=1.5):
    r = requests.get(LIST_URL, headers=HEADERS, timeout=30)
    r.raise_for_status(); r.encoding = r.apparent_encoding
    soup = BeautifulSoup(r.text, "html.parser")
    judges = []; role = None
    for el in soup.find_all(["h3","dl"]):
        if el.name=="h3":
            t = el.get_text(strip=True)
            if "長官" in t: role="長官"
            elif "判事" in t: role="判事"
            continue
        a = el.select_one("dt a")
        if not a: continue
        href = a.get("href","")
        m = re.search(r"\./?([^/]+)/index\.html$", href)
        if not m: continue
        slug = m.group(1)
        if slug in ("hanzi_itiran",): continue
        name = a.get_text(strip=True)
        dd = el.select_one("dd"); court=None
        if dd:
            cm = re.search(r"(大法廷|第[一二三]小法廷)", dd.get_text())
            court = cm.group(1) if cm else None
        judges.append({"judgeId":slug,"name":name,"nameNormalized":norm_name(name),
            "role":role,"division":court,"status":"現職",
            "detailUrl":urljoin(LIST_URL, href),"reading":None,"birth":None,"career":[]})
    if fetch_detail:
        for j in judges:
            try:
                j.update(parse_judge_page(j["detailUrl"]))
            except Exception as e:
                print(f"  詳細取得失敗 {j['name']}: {e}", file=sys.stderr)
            time.sleep(sleep)
    return judges

